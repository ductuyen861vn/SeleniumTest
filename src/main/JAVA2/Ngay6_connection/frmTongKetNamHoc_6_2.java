    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        if(txtHoTen.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Moi Nhap Ho Va ten");
            return;
        }
        if(txtTBHK1.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Moi Nhap Diem TBHK1");
            return;
        }
        if(txtTBHK2.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Moi Nhap Diem TBHK2");
            return;
        }
        
        String hoTen = txtHoTen.getText();
        double diemHK1= Double.parseDouble(txtTBHK1.getText());
        double diemHK2= Double.parseDouble(txtTBHK2.getText());
        int khoi = Integer.parseInt(cboKhoi.getSelectedItem().toString());
        double diemTBCN;
        
        try(DataOutputStream out = new DataOutputStream(new BufferedOutputStream(new FileOutputStream(url, true)))){
        out.writeUTF(hoTen);
        out.writeInt(khoi);
        out.writeDouble(diemHK1);
        out.writeDouble(diemHK2);
        diemTBCN=(diemHK1+diemHK2*2)/3;
        DecimalFormat df = new DecimalFormat("###.##");//dinh dang cho double
        diemTBCN=Double.parseDouble(df.format(diemTBCN));//dinh dang cho double ve mau ###.##
        Tmodel.addRow(new Object[]{hoTen, khoi, diemHK1, diemHK2, diemTBCN});
        JOptionPane.showMessageDialog(rootPane, "Add thanh cong!");
        } catch (IOException ex) {
            //Logger.getLogger(frmTongKetNamHoc_6_2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnOpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOpenActionPerformed
        // TODO add your handling code here:
        File file = new File(url);
        Tmodel.setRowCount(0);
        if(!file.isFile()){
        JOptionPane.showMessageDialog(rootPane, "File nay Khong ton tai");
        return;
        }
        
        String hoten;
        int khoi;
        double diemTBHK1, diemTBHK2, diemTBCN;
        
        try(DataInputStream in = new DataInputStream(new BufferedInputStream(new FileInputStream(url)))){
        while(true){//doc den cuoi tap tin
        hoten=in.readUTF();
        khoi= in.readInt();
        diemTBHK1=in.readDouble();
        diemTBHK2=in.readDouble();
        diemTBCN=(diemTBHK1+diemTBHK2*2)/3;
        DecimalFormat df = new DecimalFormat("###.##");//dinh dang cho double
        diemTBCN=Double.parseDouble(df.format(diemTBCN));//dinh dang cho double ve mau ###.##
        Tmodel.addRow(new Object[]{hoten, khoi, diemTBHK1, diemTBHK2, diemTBCN});
        
        
        }
        } catch (IOException ex) {
            //Logger.getLogger(frmTongKetNamHoc_6_2.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnOpenActionPerformed

