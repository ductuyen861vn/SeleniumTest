    private void btnSumaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumaryActionPerformed
        // TODO add your handling code here:
        int soHKGioi=0, soHKKha=0, soHSTB=0, soHSYeu=0;
        double diemTBCN;
        for(int i = 0; i<Tmodel.getRowCount(); i++){
        diemTBCN=Double.parseDouble(Tmodel.getValueAt(i, 4).toString());
        if(diemTBCN>=8)soHKGioi++;
        else if (diemTBCN>=7)soHKKha++;
        else if (diemTBCN>=5)soHSTB++;
        else soHSYeu++;
        }
        String chuoi = "soHKGioi: " + soHKGioi + "\n" +
                "soHKKha: " + soHKKha + "\n"+
                "soHSTB: " + soHSTB + "\n" +
                "soHSYeu: " + soHSYeu + "\n";
        JOptionPane.showMessageDialog(rootPane, chuoi);
        
    }//GEN-LAST:event_btnSumaryActionPerformed

