    private void btnThemHinhTronActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemHinhTronActionPerformed
        // TODO add your handling code here:
        
        double r = Double.parseDouble(txtBanKinh.getText());
        Ngay5_HinhTron ht= new Ngay5_HinhTron(r);
        listHinhTron.themHinh(ht); //Tuong dong vs .add o List, da declare method ben class Generic_Hinh
        if (rbtHT.isSelected()){
        Model.addElement(ht);
        txtTongSL.setText(listHinhTron.soLuongHinh()+"");
        txtDTMAX.setText(listHinhTron.dienThichMax()+"");
        }
    }//GEN-LAST:event_btnThemHinhTronActionPerformed

    private void btnThemHinhChuNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemHinhChuNhatActionPerformed
        // TODO add your handling code here:
        double d = Double.parseDouble(txtDai.getText());
        double r = Double.parseDouble(txtRong.getText());
        Ngay5_HinhCN hcn = new Ngay5_HinhCN(d, r);
        
        listHinhCN.themHinh(hcn);
        if (rbtHCN.isSelected()){
        Model.addElement(hcn);
        txtTongSL.setText(listHinhCN.soLuongHinh()+"");
        txtDTMAX.setText(listHinhCN.dienThichMax()+"");
        }
    }//GEN-LAST:event_btnThemHinhChuNhatActionPerformed

    private void rbtHTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbtHTActionPerformed
        // TODO add your handling code here:
        
        if (rbtHT.isSelected()){
        Model.clear();
        listHinhTron.getHinhs().forEach(hinh ->Model.addElement(hinh));
        txtTongSL.setText(listHinhTron.soLuongHinh()+"");
        txtDTMAX.setText(listHinhTron.dienThichMax()+"");
        }
        
    }//GEN-LAST:event_rbtHTActionPerformed

