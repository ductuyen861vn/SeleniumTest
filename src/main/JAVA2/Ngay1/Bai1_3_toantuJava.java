    //Nhom Lon nho
        public void sosanh(int so1, int so2){
            if(so1>so2)
                txtKetQua.setText(so1 + " lon hon "+ so2);
            else if(so1<so2)
                txtKetQua.setText(so1 + " be hon "+ so2);
            else
                txtKetQua.setText(so1 + " bang "+ so2);
    }
        
        
        
        
