    private void btnTinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhActionPerformed
        // TODO add your handling code here:
        try{
        int so1= Integer.parseInt(txtSo1.getText());
        int so2= Integer.parseInt(txtSo2.getText());
        
        switch (cbNhom.getSelectedIndex()){
            case 1:
                switch (cbToanTu.getSelectedIndex()){
                    case 0: txtKetQua.setText(String.format("%.0f",cong.operator(so1, so2)));break;
                    case 1: txtKetQua.setText(String.format("%.0f",tru.operator(so1, so2)));break;
                    case 2: txtKetQua.setText(String.format("%.0f",nhan.operator(so1, so2)));break;
                    case 3: txtKetQua.setText(String.format("%.0f",chia.operator(so1, so2)));break;
                    case 4: txtKetQua.setText(String.format("%.0f",chiadu.operator(so1, so2)));break;
                    default: JOptionPane.showMessageDialog(rootPane, "Moi chon toan tu");break;
            }break;
            case 2:
                    sosanh(so1, so2);break;
            case 3: case 4:
                    JOptionPane.showMessageDialog(rootPane, "Chuc nag chua Dev");break;
                    
            default:
                    JOptionPane.showMessageDialog(rootPane, "Moi chon Nhom");break;
        }
           
        
       
        }   catch (NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane, "Moi Nhap so");
        }
        
            
        
        
        
        
    }//GEN-LAST:event_btnTinhActionPerformed

