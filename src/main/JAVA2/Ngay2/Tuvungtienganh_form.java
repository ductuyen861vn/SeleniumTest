    private void lstTuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstTuMouseClicked
        // TODO add your handling code here:
        String selectedWord = lstTu.getSelectedValue().toString();//lay gia tri tu Jlist
        for(Tuvungtienganh_class tu:listTuVung){
            if(tu.word.equalsIgnoreCase(selectedWord)){
            lblXuatHinh.setIcon(new ImageIcon(tu.link));
            
            }
        }
        
    }//GEN-LAST:event_lstTuMouseClicked

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(rootPane, "Ban co muon xa Hinh nay?", "Delete", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
        String selectedWord = lstTu.getSelectedValue().toString();//lay gia tri tu Jlist
        for(Tuvungtienganh_class tu:listTuVung){
            if(tu.word.equalsIgnoreCase(selectedWord)){
                listTuVung.remove(tu);
                model.removeElement(tu.getWord());
                lblXuatHinh.setIcon(null);
                break;
            }
        }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

