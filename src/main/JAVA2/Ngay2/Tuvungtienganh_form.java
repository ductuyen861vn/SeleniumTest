    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        Tuvungtienganh_class tuvung = new Tuvungtienganh_class(txtTuTiengAnh.getText(), txtLinkHinh.getText());
        listTuVung.add(tuvung);
        listTuVung.sort((s1, s2)->s1.word.compareToIgnoreCase(s2.word));
        model.clear();//Clear model
        //add tu vung vao model
        for(Tuvungtienganh_class tu:listTuVung){
            model.addElement(tu.word);
        }
        JOptionPane.showMessageDialog(rootPane, "Tu add Thanh COng");
        lstTu.setSelectedValue(tuvung.getWord(), rootPaneCheckingEnabled);//set forcus vao tu vua moi them ben Jlist
        lblThemHinh.setIcon(new ImageIcon(tuvung.link));//set Hinh
        lblXuatHinh.setIcon(new ImageIcon(tuvung.link));
        
        
        
    }//GEN-LAST:event_btnThemActionPerformed

