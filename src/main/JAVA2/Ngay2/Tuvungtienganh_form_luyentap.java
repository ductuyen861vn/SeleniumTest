    private void btnThemTuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemTuActionPerformed
        // TODO add your handling code here:
        
        Tuvungtienganh_class word = new Tuvungtienganh_class(txtWord.getText(), txtPath.getText());
        Listtuvung.add(word);
        //Biểu thức Lambda sắp xếp
        Listtuvung.sort((s1, s2)->s1.word.compareToIgnoreCase(s2.word));
        //Clear model trc khi add lại
        Model.clear();
        //Add word vô Model
        for (Tuvungtienganh_class tuvung:Listtuvung ){
            Model.addElement(tuvung.word);
        }
        JOptionPane.showMessageDialog(this, "Add thành công!");
        lstWord.setSelectedValue(word.getWord(), rootPaneCheckingEnabled);//add highlight vào từ vừa mới add
        lblInputImage.setIcon(new ImageIcon(word.link));
        lblOutputImage.setIcon(new ImageIcon(word.link));
    }//GEN-LAST:event_btnThemTuActionPerformed

