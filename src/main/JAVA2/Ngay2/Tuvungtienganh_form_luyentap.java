    private void lstWordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstWordMouseClicked
        // TODO add your handling code here:
        String world = lstWord.getSelectedValue().toString();
        for(Tuvungtienganh_class tuvung:Listtuvung){
            if(tuvung.word.equalsIgnoreCase(world))
                lblOutputImage.setIcon(new ImageIcon(tuvung.link));
            
        }
        
        
        
    }//GEN-LAST:event_lstWordMouseClicked

    private void btnDeleteWordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteWordActionPerformed
        // TODO add your handling code here:
        String world = lstWord.getSelectedValue().toString();
        for(Tuvungtienganh_class tuvung:Listtuvung){
            if(tuvung.word.equalsIgnoreCase(world)) {
            Listtuvung.remove(tuvung);
            Model.removeElement(tuvung.getWord());
            lblOutputImage.setIcon(null);
            break;
            }
                
        }
    }//GEN-LAST:event_btnDeleteWordActionPerformed

