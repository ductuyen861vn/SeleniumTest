    private void btnTinhDiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhDiemActionPerformed
        // TODO add your handling code here:
        float diem= Float.parseFloat(txtDiem.getText());
        
        //Ketqua
        String ketQua = (diem>=5)?"Dau":"Rot";
        txtKetQua.setText(ketQua);
        
        //Xep loai
        if(diem<0 || diem >10)
        {    
        JOptionPane.showMessageDialog(rootPane, "Nhap Sai Diem");
        txtKetQua.setText("Null");
        txtXepLoai.setText("Null");
        }
        else if (diem<=6)
        txtXepLoai.setText("TB");
        else if (diem<=7)
        txtXepLoai.setText("Kha");
        else if (diem<=8)
        txtXepLoai.setText("Kha Gioi");
        else if (diem<=9)
        txtXepLoai.setText("Gioi");
        else if (diem<=10)
        txtXepLoai.setText("Xuat Sac");
        
       
        
        
    }//GEN-LAST:event_btnTinhDiemActionPerformed

