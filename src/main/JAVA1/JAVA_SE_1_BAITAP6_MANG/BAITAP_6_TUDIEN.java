    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
        mainArray[mainArrayIndex]=txtWord.getText()+ ":" + txtMeaning.getText();
        Arrays.sort(mainArray,0, mainArrayIndex+1);
        //Xuat ra text Area
        String chuoi="";
        for (int i=0; i<=mainArrayIndex; i++){
         chuoi += mainArray[i] + "\n";
        }
        txtaXuat.setText(chuoi);
        mainArrayIndex++;
        }catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(rootPane, "Da vuot qua gioi han do dai "+mainArrayIndex+" tu cua tu dien");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

