    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        boolean tim=false;
        for (int i=0; i<mainArrayIndex; i++){
        String arrayTim []=mainArray[i].split(":");
        
        if (arrayTim[0].equalsIgnoreCase(txtWord.getText())){
            txtMeaning.setText(arrayTim[1]);
        tim = true;
        break; //Break if
        }
        }
        if(tim==false){
            JOptionPane.showMessageDialog(rootPane, "Khong co tu nay tron tu dien");
            txtMeaning.setText(null);}
            
        
    }//GEN-LAST:event_jButton3ActionPerformed

