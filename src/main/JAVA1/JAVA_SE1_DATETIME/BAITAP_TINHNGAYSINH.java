    private void btnTinhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTinhActionPerformed
        try {
            // TODO add your handling code here:
            Date ngay = CLASS_CARLENDER.stringToDate(txtBirthDay.getText());
            Calendar cal = Calendar.getInstance();
            cal.setTime(ngay);
            MonthDay birthDay = MonthDay.of(cal.get(Calendar.MONTH)+1, cal.get(Calendar.DAY_OF_MONTH));
            MonthDay currentDay = MonthDay.now();
            if (birthDay.equals(currentDay))
                    lblHinh.setIcon(new ImageIcon("scr/hinh_SN/HappyBirthday.jpg"));
            else if (birthDay.isBefore(currentDay))
                    lblHinh.setIcon(new ImageIcon("scr/hinh_SN/Waiting.jpg"));
            else 
                    lblHinh.setIcon(new ImageIcon("scr/hinh_SN/SeeYouNextYear.jpg"));
            
            
        } catch (ParseException ex) {
            //Logger.getLogger(BAITAP_TINHNGAYSINH.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Nhap Sai Ngay Thang");
        }
        
        
        
    }//GEN-LAST:event_btnTinhActionPerformed

