    private void btnKhoiTaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKhoiTaoActionPerformed
        // TODO add your handling code here:
        //Bo Binh
        String tenBB=txtTenBB.getText();
        int sucManhBB=Integer.parseInt(txtSucManhBB.getText());
        String vuKhiBB=txtVuKhiBB.getText();
        
        //Ki Binh
        String tenKB=txtTenKB.getText();
        int sucManhKB=Integer.parseInt(txtSucManhKB.getText());
        String vuKhiKB=txtVuKhiKB.getText();
        
        
        bb = new BAITAP_9_1_bobinh(tenBB, vuKhiBB, sucManhBB);
        kb = new BAITAP_9_1_kibinh (tenKB, vuKhiKB, sucManhKB);
        
        String chuoi =" Bo Binh: "+ bb.getTen()+ " vu khi: "+bb.getVuKhi()+" Suc manh: "+bb.getSucManh() + "\n" +
                " Ki Binh: " + kb.getTen() + " vu khi: "+kb.getVuKhi()+ " Suc Manh: "+ kb.getSucManh();
        
        JOptionPane.showMessageDialog(rootPane, chuoi);
        
    }//GEN-LAST:event_btnKhoiTaoActionPerformed

    private void btnBBDanhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBBDanhActionPerformed
        // TODO add your handling code here:
        
        if(bb.sucManh>0){
            if (kb.sucManh>0){
            kb.phuongThucChienDau();
            txtSucManhKB.setText(kb.getSucManh()+"");
            JOptionPane.showMessageDialog(rootPane, "Suc manh BB: " + bb.getSucManh() + "Suc Manh KB: "+ kb.getSucManh());
            }else
                JOptionPane.showConfirmDialog(rootPane, "Ki Binh Da Thua");
        }else
            JOptionPane.showConfirmDialog(rootPane, "Suc manh BB da het, BB da thua");
        
    }//GEN-LAST:event_btnBBDanhActionPerformed

    private void btnKBDanhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKBDanhActionPerformed
        // TODO add your handling code here:
        
        if(kb.sucManh>0){
            if (bb.sucManh>0){
            bb.phuongThucChienDau();
            txtSucManhBB.setText(bb.getSucManh()+"");
            JOptionPane.showMessageDialog(rootPane, "Suc manh BB: " + bb.getSucManh() + "Suc Manh KB: "+ kb.getSucManh());
            }else
                JOptionPane.showConfirmDialog(rootPane, "Bo Binh Da Thua");
        }else
            JOptionPane.showConfirmDialog(rootPane, "Suc manh KB da het, KB da thua");
        
    }//GEN-LAST:event_btnKBDanhActionPerformed


